def generate_engine_file(
    system_id: str,
    manifest: dict,
    engine_dir: Path,
    force: bool = False,
) -> Path:
    """Generate the engine __init__.py file.

    Args:
        system_id: The system identifier.
        manifest: Parsed manifest dict.
        engine_dir: Target directory (codex/games/{system_id}/).
        force: If True, overwrite existing file.

    Returns:
        Path to the generated file.

    Raises:
        SystemExit: If file exists and force is False.
    """
    target = engine_dir / "__init__.py"
    if target.exists() and not force:
        print(
            f"ERROR: {target} already exists. Use --force to overwrite.",
            file=sys.stderr,
        )
        sys.exit(2)

    engine_dir.mkdir(parents=True, exist_ok=True)

    primary_loop = manifest.get("primary_loop", "scene_navigation")
    traits: List[str] = manifest.get("engine_traits", [])
    char_stats: List[str] = manifest.get("character_stats", [])
    display_name: str = manifest.get("display_name", system_id.title())
    system_family: str = manifest.get("system_family", system_id.upper())

    class_name = _to_class_name(system_id)
    extra_imports = _build_extra_imports(traits)
    char_stat_fields = _build_char_stat_fields(char_stats)
    ruler = "=" * (len(system_id) + 30)

    if primary_loop == "spatial_dungeon":
        base_class = "SpatialEngineBase"
        template = _SPATIAL_ENGINE_TEMPLATE
    else:
        base_class = "NarrativeEngineBase"
        template = _NARRATIVE_ENGINE_TEMPLATE

    content = template.format(
        system_id=system_id,
        class_name=class_name,
        base_class=base_class,
        display_name=display_name,
        system_family=system_family,
        extra_imports=extra_imports if extra_imports else "",
        char_stat_fields=char_stat_fields,
        ruler=ruler,
    )

    target.write_text(content, encoding="utf-8")
    try:
        display_path = target.relative_to(_PROJECT_ROOT)
    except ValueError:
        display_path = target
    print(f"  Created: {display_path}")
    return target


def generate_config_template(
    system_id: str,
    category: str,
    template_data: dict,
    force: bool = False,
) -> Path:
    """Generate an empty config JSON template.

    Args:
        system_id: The system identifier.
        category: Config category (e.g. 'bestiary', 'loot').
        template_data: The template dict to serialize.
        force: If True, overwrite existing file.

    Returns:
        Path to the generated file.
    """
    config_dir = _PROJECT_ROOT / "config" / category
    config_dir.mkdir(parents=True, exist_ok=True)
    target = config_dir / f"{system_id}.json"

    if target.exists() and not force:
        print(f"  Skipped (exists): config/{category}/{system_id}.json")
        return target

    filled = json.loads(
        json.dumps(template_data).replace('"{system_id}"', f'"{system_id}"')
    )
    target.write_text(
        json.dumps(filled, indent=2, ensure_ascii=False) + "\n",
        encoding="utf-8",
    )
    print(f"  Created: config/{category}/{system_id}.json")
    return target


def generate_test_file(
    system_id: str,
    manifest: dict,
    tests_dir: Path,
    force: bool = False,
) -> Path:
    """Generate the smoke test file.

    Args:
        system_id: The system identifier.
        manifest: Parsed manifest dict.
        tests_dir: The tests/ directory.
        force: If True, overwrite existing file.

    Returns:
        Path to the generated file.
    """
    target = tests_dir / f"test_{system_id}_engine.py"

    if target.exists() and not force:
        print(
            f"  Skipped (exists): tests/test_{system_id}_engine.py",
        )
        return target

    display_name: str = manifest.get("display_name", system_id.title())
    class_name = _to_class_name(system_id)

    content = _TEST_TEMPLATE.format(
        system_id=system_id,
        class_name=class_name,
        display_name=display_name,
    )

    tests_dir.mkdir(parents=True, exist_ok=True)
    target.write_text(content, encoding="utf-8")
    print(f"  Created: tests/test_{system_id}_engine.py")
    return target


