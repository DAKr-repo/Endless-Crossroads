def find_manifest(system_id: str) -> Optional[Path]:
    """Locate system_manifest.json for a given system_id.

    Search order:
        1. codex/games/{system_id}/system_manifest.json
        2. vault/{system_id}/system_manifest.json
        3. Recursive vault search for any system_manifest.json with matching system_id

    Args:
        system_id: The system identifier to search for.

    Returns:
        Path to the manifest file, or None if not found.
    """
    candidates = [
        _PROJECT_ROOT / "codex" / "games" / system_id / "system_manifest.json",
        _PROJECT_ROOT / "vault" / system_id / "system_manifest.json",
    ]
    for path in candidates:
        if path.exists():
            return path

    vault = _PROJECT_ROOT / "vault"
    if vault.exists():
        for manifest_path in vault.rglob("system_manifest.json"):
            try:
                data = json.loads(manifest_path.read_text(encoding="utf-8"))
                if data.get("system_id") == system_id:
                    return manifest_path
            except (json.JSONDecodeError, OSError):
                continue

    return None


def load_manifest(manifest_path: Path) -> dict:
    """Load and parse a system manifest JSON file.

    Args:
        manifest_path: Path to system_manifest.json.

    Returns:
        Parsed manifest dict.

    Raises:
        SystemExit: If the file cannot be read or parsed.
    """
    try:
        data = json.loads(manifest_path.read_text(encoding="utf-8"))
        return data
    except (json.JSONDecodeError, OSError) as exc:
        print(f"ERROR: Cannot read manifest at {manifest_path}: {exc}", file=sys.stderr)
        sys.exit(1)


