def main() -> None:
    """Entry point for the scaffold_engine CLI."""
    parser = argparse.ArgumentParser(
        description="Scaffold a new game engine from a system manifest.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=dedent(__doc__),
    )
    parser.add_argument("system_id", help="System identifier (e.g. 'myworld')")
    parser.add_argument(
        "--force",
        action="store_true",
        default=False,
        help="Overwrite existing engine file if present",
    )
    parser.add_argument(
        "--manifest",
        type=str,
        default=None,
        help="Explicit path to system_manifest.json (overrides auto-search)",
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        default=False,
        help="Show what would be generated without writing files",
    )

    args = parser.parse_args()
    system_id: str = args.system_id

    print(f"Scaffolding engine for: {system_id}")

    # Locate manifest
    if args.manifest:
        manifest_path = Path(args.manifest).resolve()
        if not manifest_path.exists():
            print(f"ERROR: Manifest not found at {args.manifest}", file=sys.stderr)
            sys.exit(1)
    else:
        manifest_path = find_manifest(system_id)
        if manifest_path is None:
            print(
                f"ERROR: No system_manifest.json found for '{system_id}'.\n"
                f"  Searched: codex/games/{system_id}/, vault/{system_id}/, vault/**",
                file=sys.stderr,
            )
            sys.exit(1)

    print(f"  Manifest: {manifest_path.relative_to(_PROJECT_ROOT)}")
    manifest = load_manifest(manifest_path)

    if args.dry_run:
        print("\n[DRY RUN] Would generate:")
        print(f"  codex/games/{system_id}/__init__.py")
        print(f"  config/bestiary/{system_id}.json")
        print(f"  config/loot/{system_id}.json")
        print(f"  tests/test_{system_id}_engine.py")
        print(f"\nManifest summary:")
        print(f"  display_name: {manifest.get('display_name', '?')}")
        print(f"  engine_type:  {manifest.get('engine_type', '?')}")
        print(f"  primary_loop: {manifest.get('primary_loop', '?')}")
        print(f"  traits:       {manifest.get('engine_traits', [])}")
        print(f"  resolution:   {manifest.get('resolution_mechanic', '?')}")
        sys.exit(0)

    engine_dir = _PROJECT_ROOT / "codex" / "games" / system_id
    tests_dir = _PROJECT_ROOT / "tests"

    print("\nGenerating files:")
    generate_engine_file(system_id, manifest, engine_dir, force=args.force)
    generate_config_template(
        system_id,
        "bestiary",
        {
            "_comment": "AUTO-GENERATED by scaffold_engine.py -- populate from source PDF",
            "system_id": system_id,
            "tiers": {"1": [], "2": [], "3": [], "4": []},
        },
        force=args.force,
    )
    generate_config_template(
        system_id,
        "loot",
        {
            "_comment": "AUTO-GENERATED by scaffold_engine.py -- populate from source PDF",
            "system_id": system_id,
            "tiers": {"1": [], "2": [], "3": [], "4": []},
        },
        force=args.force,
    )
    generate_test_file(system_id, manifest, tests_dir, force=args.force)

    print(f"\nDone! Next steps:")
    print(f"  1. Customize codex/games/{system_id}/__init__.py — implement _get_adapter()")
    print(f"  2. Populate config/bestiary/{system_id}.json from source PDF")
    print(f"  3. Populate config/loot/{system_id}.json from source PDF")
    print(f"  4. Run: python -m pytest tests/test_{system_id}_engine.py -v")


if __name__ == "__main__":
    main()
