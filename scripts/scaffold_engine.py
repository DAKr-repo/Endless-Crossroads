def _to_class_name(system_id: str) -> str:
    """Convert system_id to a CamelCase class name prefix.

    E.g. 'my_system' -> 'MySystem', 'pbta' -> 'Pbta'

    Args:
        system_id: The system identifier string.

    Returns:
        CamelCase string.
    """
    return "".join(word.capitalize() for word in system_id.replace("-", "_").split("_"))


def _build_extra_imports(traits: List[str]) -> str:
    """Build import lines for the traits declared in the manifest.

    Args:
        traits: List of trait name strings from manifest.

    Returns:
        Newline-separated import statements string.
    """
    imports: Dict[str, List[str]] = {}
    for trait in traits:
        if trait in TRAIT_WIRING:
            module, symbol = TRAIT_WIRING[trait]
            imports.setdefault(module, []).append(symbol)

    lines = []
    for module, symbols in sorted(imports.items()):
        lines.append(f"from {module} import {', '.join(sorted(symbols))}")
    return "\n".join(lines)


def _build_char_stat_fields(char_stats: List[str]) -> str:
    """Build dataclass field lines for declared character_stats.

    Args:
        char_stats: List of stat name strings from manifest.

    Returns:
        Indented dataclass field lines (4-space indent).
    """
    if not char_stats:
        return "    # TODO: add character stat fields"
    lines = []
    for stat in char_stats:
        lines.append(f"    {stat}: int = 0")
    return "\n".join(lines)


